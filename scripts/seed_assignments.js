async function seedAssignments() {
  console.log('[seed_assignments] Starting...')

  for (const entry of data) {
    const { rows: lessonRows } = await pool.query(
      'SELECT id FROM lessons WHERE slug = $1',
      [entry.lessonSlug]
    )

    if (lessonRows.length === 0) {
      console.warn(`[seed_assignments] WARNING: lesson not found: ${entry.lessonSlug}`)
      continue
    }

    const lessonId = lessonRows[0].id

    for (const assignment of entry.assignments) {
      await pool.query(
        `INSERT INTO assignments (lesson_id, title, description, hints, "order")
         VALUES ($1, $2, $3, $4, $5)
         ON CONFLICT (lesson_id, "order") DO UPDATE
           SET title       = EXCLUDED.title,
               description = EXCLUDED.description,
               hints       = EXCLUDED.hints`,
        [
          lessonId,
          assignment.title,
          assignment.description,
          JSON.stringify(assignment.hints),
          assignment.order,
        ]
      )
    }

    console.log(`[seed_assignments] ✓ ${entry.lessonSlug} (${entry.assignments.length})`)
  }

  console.log('[seed_assignments] Done.')
  await pool.end()
}

seedAssignments().catch((err) => {
  console.error('[seed_assignments] FAILED:', err)
  process.exit(1)
})
